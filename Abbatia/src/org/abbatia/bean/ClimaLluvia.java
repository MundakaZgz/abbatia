public class ClimaLluvia {
    private int idClima;
    private int minLlubia;
    private int maxLlubia;

    public int getIdClima() {
        return idClima;
    }

    public void setIdClima(int idClima) {
        this.idClima = idClima;
    }

    public int getMinLlubia() {
        return minLlubia;
    }

    public void setMinLlubia(int minLlubia) {
        this.minLlubia = minLlubia;
    }

    public int getMaxLlubia() {
        return maxLlubia;
    }

    public void setMaxLlubia(int maxLlubia) {
        this.maxLlubia = maxLlubia;
    }
}
